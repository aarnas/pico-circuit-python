def convertLine(line):
    newline = []
    # loop on each key - the filter removes empty values
    for key in filter(None, line.split(" ")):
        key = key.upper()
        # find the keycode for the command in the list
        command_keycode = duckyCommands.get(key, None)
        if command_keycode is not None:
            # if it exists in the list, use it
            newline.append(command_keycode)
        elif hasattr(Keycode, key):
            # if it's in the Keycode module, use it (allows any valid keycode)
            newline.append(getattr(Keycode, key))
        else:
            # if it's not a known key name, show the error for diagnosis
            print(f"Unknown key: <{key}>")
    return newline

def runScriptLine(line):
    for k in line:
        kbd.press(k)
    kbd.release_all()

def sendString(line):
    layout.write(line)

def parseLine(line):
    if(line[0:3] == "REM"):
        pass
    elif(line[0:6] == "STRING"):
        sendString(line[7:])
    elif(line[0:6] == "MOUSEY"):
        mouse.move(y=int(line[7:]))
    elif(line[0:6] == "MOUSEX"):
        mouse.move(x=int(line[7:]))
    else:
        newScriptLine = convertLine(line)
        runScriptLine(newScriptLine)

f = open('text.txt',"r",encoding='utf-8')

while True:
    data = uart.read(32)
    if data is not None:
        data_string = ''.join([chr(b) for b in data])
        data_string_length = len(data_string)
        if data_string_length > 2:
            parseLine(data_string[0:data_string_length-2])
            data_string_length = 0
            text = f.read()
            uart.write(b'' + text.encode() + b'')

